function addNumbers(a, b) {
    return a + b;
}

let results = addNumbers(5, 3) + addNumbers(2, 4);
console.log(results);
// In this example, the addNumbers function takes two parameters, a and b, and returns their sum. When we call addNumbers(5, 3), it returns the value 8, which is then stored in the variable results. Finally, we log the value of results to the console, which outputs 8.

