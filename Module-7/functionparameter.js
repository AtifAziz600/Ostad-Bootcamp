function add(a , b) {
    console.log(a + b);
}
add(5, 3);
//parameter is a variable that is used in a function to receive a value when the function is called. In the above example, a and b are parameters of the add function. When we call add(5, 3), the values 5 and 3 are passed as arguments to the function, and they are assigned to the parameters a and b respectively. The function then adds these two values and logs the result, which is 8.